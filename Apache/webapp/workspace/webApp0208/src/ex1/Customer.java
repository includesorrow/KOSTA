package ex1;

public class Customer {

	
	public void setName(String name) {
		Name = name;
	}
	public void setEmail(String email) {
		Email = email;
	}
	public void setPhone(String phone) {
		Phone = phone;
	}
	public String getName() {
		return Name;
	}
	public String getEmail() {
		return Email;
	}
	public String getPhone() {
		return Phone;
	}
	String Name;
	String Email;
	String Phone;
	
}
